package Gun15;

public class _03_Forloop {
    public static void main(String[] args) {
        //-0 dan 10 (dahil) kadar sayilari ekrana yazdiriniz.
        // 10 dan 0 (dahil) kadar sayilari ekrana yazdiriniz.

        for (int i=0; i<=10; i++){
            System.out.println("0 dan 10 (dahil) kadar sayilar="+i);
        }
        for (int j=10; j>=0; j--){
            System.out.println("10 dan 0 (dahil) kadar sayilar.="+j);

        }
    }
}
