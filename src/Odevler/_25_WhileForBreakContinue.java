package Odevler;

public class _25_WhileForBreakContinue {
    public static void main(String[] args) {
       // 0 ile 100 arasındaki bütün çift sayıları yazdırabileceğiniz bir kod yazınız.
        for (int i=0; i<=100; i++){
            if (i%2==0){
                System.out.println("i="+i);
            }
        }


    }
}
