package Gun33._02_Encapsulation;

public class araba {

    private  String renk;
    private  String model;
    private  int motohacmi;
    private  int kapisayisi;

    public araba() {
        this.renk = renk;
        this.model = model;
        this.motohacmi = motohacmi;
        this.kapisayisi = kapisayisi;
    }

    public String getRenk() {
        return renk;
    }

    public void setRenk(String renk) {
        this.renk = renk;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getMotohacmi() {
        return motohacmi;
    }

    public void setMotohacmi(int motohacmi) {
        this.motohacmi = motohacmi;
    }

    public int getKapisayisi() {
        return kapisayisi;
    }

    public void setKapisayisi(int kapisayisi) {
        this.kapisayisi = kapisayisi;
    }
}
